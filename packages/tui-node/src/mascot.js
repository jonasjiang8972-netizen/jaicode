// ─── Fixed-width frame renderer ────────────────────────
const FRAME_WIDTH = 28
const FRAME_HEIGHT = 14

function padFrame(lines) {
  return lines.map(l => {
    const visibleLen = l.replace(/\x1B\[[0-9;]*[mK]/g, '').length
    return l + ' '.repeat(Math.max(0, FRAME_WIDTH - visibleLen))
  })
}

function colorize(frame) {
  return frame.map(line => line
    .replace(/▓/g, '\x1B[38;2;255;140;0m▓\x1B[0m')
    .replace(/█/g, '\x1B[38;2;100;100;100m█\x1B[0m')
    .replace(/▒/g, '\x1B[38;2;60;60;60m▒\x1B[0m')
    .replace(/◉/g, '\x1B[38;2;0;255;200m◉\x1B[0m')
    .replace(/[◎◯○★✦]/g, '\x1B[38;2;255;255;0m·\x1B[0m')
  )
}

